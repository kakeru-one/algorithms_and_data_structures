#include <bits/stdc++.h>
using namespace std;

int main() {
  int A, B;
  cin >> A >> B;

  // ここにプログラムを追記
  cout << "A:";
  int index = 0;
  while (index < A) {
    cout << "]";
    index++;
  }
  cout << endl;
  
  cout << "B:";
  index = 0;
  while (index < B) {
    cout << "]";
    index++;
  }
  cout << endl;
}
